void initStartScreen() {
	REG_BG0CNT = BG_CHARBLOCK(0) | BG_SCREENBLOCK(7) | BG_4BPP | BG_SIZE_SMALL;

    DMANow(3, startScreenPal, PALETTE, 256);
    DMANow(3, startScreenTiles, &CHARBLOCK[0], startScreenTilesLen / 2);
    DMANow(3, startScreenMap, &SCREENBLOCK[7], startScreenMapLen / 2);

    DMANow(3, startSpritePal, SPRITEPALETTE, 256);
    DMANow(3, startSpriteTiles, &CHARBLOCK[4], 8192 + 8192/2);
	hideSprites();

	pointer.worldCol = 84;
	pointer.worldRow = 85;
	shadowOAM[0].attr0 = (pointer.worldRow & ROWMASK) | ATTR0_4BPP | ATTR0_SQUARE;
	shadowOAM[0].attr1 = (pointer.worldCol & COLMASK) | ATTR1_SMALL;
	shadowOAM[0].attr2 = ATTR2_PALROW(0) | ATTR2_TILEID(0, 0);
}

void updateStartScreen() {
	// start
	if (BUTTON_PRESSED(BUTTON_UP)) {
		pointer.worldCol = 84;
		pointer.worldRow = 85;
    }

	// instructions
	if (BUTTON_PRESSED(BUTTON_DOWN)) {
		pointer.worldCol = 62;
		pointer.worldRow = 100;
    }

	// start the game
	if (pointer.worldRow == 85 && BUTTON_PRESSED(BUTTON_A)) {
		playGame = 1;
	}

	// go to instructions
	if (pointer.worldRow == 100 && BUTTON_PRESSED(BUTTON_A)) {
		howGame = 1;
	}
}

void drawStartScreen() {
	shadowOAM[0].attr0 = (pointer.worldRow & ROWMASK) | ATTR0_4BPP | ATTR0_SQUARE;
	shadowOAM[0].attr1 = (pointer.worldCol & COLMASK) | ATTR1_SMALL;
	shadowOAM[0].attr2 = ATTR2_PALROW(0) | ATTR2_TILEID(0, 0);

	DMANow(3, shadowOAM, OAM, 512);
}

